import json
import os

DATA_DIR = "app/src/main/assets/data"
ARTICLES_FILE = os.path.join(DATA_DIR, "articles.json")

new_intro_content = """# Введение

Добро пожаловать в Справочник по выживанию — ваш надежный оффлайн-помощник для действий в любых экстренных ситуациях.

Главное правило этого приложения: оно всегда работает без интернета. В глухом лесу, в горах, при отключении электричества или в зоне стихийного бедствия — вся жизненно важная информация останется в вашем кармане.

## Адаптация под реалии

В основе этого проекта лежат материалы открытого англоязычного проекта SurvivalManual, который, в свою очередь, базировался на классическом руководстве по выживанию армии США (FM 21-76). Мы выражаем огромную благодарность создателям оригинала.

Мы не просто переводим статьи, а полностью адаптируем их под русскоязычные реалии:

• Климат и география: Упор на выживание в условиях суровых зим, тайги, степей и болот.
• Флора и фауна: Инструкции по защите от клещей, медведей, волков и гадюк, а не от тропических скорпионов; справочник по нашим местным съедобным и ядовитым грибам/ягодам.
• Законодательство и специфика: Особенности оказания первой помощи, вызова экстренных служб (112) и легальные аспекты использования инструментов и средств самообороны.

## Инструкции на все случаи жизни

Выживание сегодня — это не только умение развести костер трением в диком лесу. Мы значительно расширили концепцию оригинального руководства. Теперь это справочник для выживания в современном мире. Здесь вы найдете инструкции для самых разных ситуаций:

• Действия при ДТП, пожарах и техногенных катастрофах.
• Выживание в городских условиях (отключение света, воды, тепла).
• Алгоритмы первой медицинской помощи до приезда скорой.
• Базовые навыки ремонта техники, ориентирования и психологии стресса.
• Связь в отсутствии интернета или прочих ограничений среды.

## Важное предупреждение

Читайте этот справочник ДО того, как окажетесь в беде. Когда счет идет на секунды, а руки трясутся от адреналина и холода, времени на чтение не будет. Знания должны быть в голове, а приложение — служить лишь шпаргалкой для уточнения деталей.

Вся информация предоставляется «как есть» в образовательных целях. Никакое руководство не заменит здравого смысла, реальных тренировок и профильных курсов (например, курсов первой помощи). Разработчики и авторы статей не несут ответственности за любые последствия применения этих знаний на практике.

Выживание — это не случайность, это подготовка. Будьте готовы ко всему!"""

with open(ARTICLES_FILE, 'r', encoding='utf-8') as f:
    articles = json.load(f)

for art in articles:
    if art['id'] == 1:
        art['content'] = new_intro_content
        break

with open(ARTICLES_FILE, 'w', encoding='utf-8') as f:
    json.dump(articles, f, ensure_ascii=False, indent=2)

print("Introduction updated successfully.")
